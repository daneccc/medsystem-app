package model;

public class Horario {
    private int id;
    private String nomeHorario;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNomeHorario() {
        return nomeHorario;
    }

    public void setNomeHorario(String nomeHorario) {
        this.nomeHorario = nomeHorario;
    }
}
